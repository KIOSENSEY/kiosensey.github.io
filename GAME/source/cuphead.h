void CupheadDraw(){

	CUPHEAD.Counter += 1;

if(GamePause == 0){
	//Idle
if(CUPHEAD.SwitchPose == 0){
	CUPHEAD.NumberTextureDuckIdle = 0;	
		CUPHEAD.Cuphead_offset = CUPHEAD.TextureIdle_offset[CUPHEAD.NumberTextureIdle];
		CUPHEAD.Cuphead = CUPHEAD.TextureIdle[CUPHEAD.NumberTextureIdle];
	if(CUPHEAD.Counter > 3){		
		if(CUPHEAD.NumberTextureIdle <= 0) CUPHEAD.SwitchTextureIdle =  1;
		if(CUPHEAD.NumberTextureIdle >= 4) CUPHEAD.SwitchTextureIdle = -1;
		CUPHEAD.NumberTextureIdle += CUPHEAD.SwitchTextureIdle;
		CUPHEAD.Counter = 0;
	}	
}
	//Run
if(CUPHEAD.SwitchPose == 1){
	CUPHEAD.NumberTextureDuckIdle = 0;	
		CUPHEAD.Cuphead_offset = CUPHEAD.TextureRunNormal_offset[CUPHEAD.NumberTextureRunNormal];
		CUPHEAD.Cuphead = CUPHEAD.TextureRunNormal[CUPHEAD.NumberTextureRunNormal];
	if(CUPHEAD.Counter > 2){
		CUPHEAD.NumberTextureRunNormal += 1;
		if(CUPHEAD.NumberTextureRunNormal >= 15)CUPHEAD.NumberTextureRunNormal = 0;
		CUPHEAD.Counter = 0;
	}	
}
	//Shoot Straight
if(CUPHEAD.SwitchPose == 3){
	CUPHEAD.NumberTextureDuckIdle = 0;
		CUPHEAD.Cuphead_offset = CUPHEAD.TextureShootStraight_offset[CUPHEAD.NumberTextureShootStraight];
		CUPHEAD.Cuphead = CUPHEAD.TextureShootStraight[CUPHEAD.NumberTextureShootStraight];
	if(CUPHEAD.Counter > 3){
		if(CUPHEAD.NumberTextureShootStraight >= 2) CUPHEAD.SwitchTextureShootStraight = -1;
		if(CUPHEAD.NumberTextureShootStraight <= 0) CUPHEAD.SwitchTextureShootStraight =  1;
		CUPHEAD.NumberTextureShootStraight += CUPHEAD.SwitchTextureShootStraight;
		CUPHEAD.Counter = 0;
}
}
	//Jump
if(CUPHEAD.SwitchPose == 2){
	CUPHEAD.NumberTextureDuckIdle = 0;
		CUPHEAD.Cuphead_offset = CUPHEAD.TextureJump_offset[CUPHEAD.NumberTextureJump];
		CUPHEAD.Cuphead = CUPHEAD.TextureJump[CUPHEAD.NumberTextureJump];
	if(CUPHEAD.Counter > 3){
		CUPHEAD.NumberTextureJump += 1;
		if(CUPHEAD.NumberTextureJump >= 6)CUPHEAD.NumberTextureJump = 0;
		CUPHEAD.Counter = 0;
	}	
}
	//Gravitation
if(CUPHEAD.SwitchGravitation == 1){
		CUPHEAD.SwitchPose = 2;
		CUPHEAD.PositionX -= CUPHEAD.FizicJump;
		CUPHEAD.FizicJump = CUPHEAD.FizicJump - 0.5;
if(CUPHEAD.FizicJump < 0){
	if(CUPHEAD.PositionX >= Ground){
		CUPHEAD.FizicJump = 9;
		CUPHEAD.SwitchGravitation = 0;
		CUPHEAD.PositionX = Ground;
	}
}		
	}

	
	if(CUPHEAD.SwitchGravitation == 0){
	if(CUPHEAD.PositionX < Ground){
		CUPHEAD.SwitchPose = 2;
		CUPHEAD.PositionX += 4;
	}
	}
	//Duck Idle
if(CUPHEAD.SwitchPose == 4){
		CUPHEAD.Cuphead_offset = CUPHEAD.TextureDuckIdle_offset[CUPHEAD.NumberTextureDuckIdle];
		CUPHEAD.Cuphead = CUPHEAD.TextureDuckIdle[CUPHEAD.NumberTextureDuckIdle];
	if(CUPHEAD.Counter > 3){
		if(CUPHEAD.NumberTextureDuckIdle <= 0) CUPHEAD.SwitchTextureDuckIdle =  1;
		if(CUPHEAD.NumberTextureDuckIdle <= 7) CUPHEAD.SwitchTextureDuckIdle =  1;
		if(CUPHEAD.NumberTextureDuckIdle >= 11) CUPHEAD.SwitchTextureDuckIdle = -1;
		CUPHEAD.NumberTextureDuckIdle += CUPHEAD.SwitchTextureDuckIdle;
		CUPHEAD.Counter = 0;
	}		
	}
	//Duck Shoot	
if(CUPHEAD.SwitchPose == 5){
	if(CUPHEAD.NumberTextureDuckIdle <= 6){
	if(CUPHEAD.Counter > 3){
		CUPHEAD.Cuphead_offset = CUPHEAD.TextureDuckIdle_offset[CUPHEAD.NumberTextureDuckIdle];
		CUPHEAD.Cuphead = CUPHEAD.TextureDuckIdle[CUPHEAD.NumberTextureDuckIdle];
		CUPHEAD.NumberTextureDuckIdle += 1;
		CUPHEAD.Counter = 0;
	}	
		}else{
		CUPHEAD.Cuphead_offset = CUPHEAD.TextureDuckShoot_offset[CUPHEAD.NumberTextureDuckShoot];
		CUPHEAD.Cuphead = CUPHEAD.TextureDuckShoot[CUPHEAD.NumberTextureDuckShoot];
	if(CUPHEAD.Counter > 1){
		if(CUPHEAD.NumberTextureDuckShoot >= 2) CUPHEAD.SwitchTextureDuckShoot = -1;
		if(CUPHEAD.NumberTextureDuckShoot <= 0) CUPHEAD.SwitchTextureDuckShoot =  1;
		CUPHEAD.NumberTextureDuckShoot += CUPHEAD.SwitchTextureDuckShoot;
		CUPHEAD.Counter = 0;
	}
}
}

	//Run Shoot
if(CUPHEAD.SwitchPose == 6){
	CUPHEAD.NumberTextureDuckIdle = 0;	
		CUPHEAD.Cuphead_offset = CUPHEAD.TextureRunShoot_offset[CUPHEAD.NumberTextureRunShoot];
		CUPHEAD.Cuphead = CUPHEAD.TextureRunShoot[CUPHEAD.NumberTextureRunShoot];
	if(CUPHEAD.Counter > 2){
		CUPHEAD.NumberTextureRunShoot += 1;
		if(CUPHEAD.NumberTextureRunShoot >= 15)CUPHEAD.NumberTextureRunShoot = 0;
		CUPHEAD.Counter = 0;
	}	
}	
	//Aim Up
if(CUPHEAD.SwitchPose == 7){
	CUPHEAD.NumberTextureDuckIdle = 0;	
	CUPHEAD.Cuphead_offset = CUPHEAD.TextureAimUp_offset[CUPHEAD.NumberTextureAimUp];
	CUPHEAD.Cuphead = CUPHEAD.TextureAimUp[CUPHEAD.NumberTextureAimUp];
	if(CUPHEAD.Counter > 3){		
		if(CUPHEAD.NumberTextureAimUp <= 0) CUPHEAD.SwitchTextureAimUp =  1;
		if(CUPHEAD.NumberTextureAimUp >= 4) CUPHEAD.SwitchTextureAimUp = -1;
		CUPHEAD.NumberTextureAimUp += CUPHEAD.SwitchTextureAimUp;
		CUPHEAD.Counter = 0;
	}	
}
	//Shoot Up
if(CUPHEAD.SwitchPose == 8){
	CUPHEAD.NumberTextureDuckIdle = 0;
		CUPHEAD.Cuphead_offset = CUPHEAD.TextureShootUp_offset[CUPHEAD.NumberTextureShootUp];
		CUPHEAD.Cuphead = CUPHEAD.TextureShootUp[CUPHEAD.NumberTextureShootUp];
	if(CUPHEAD.Counter > 3){
		if(CUPHEAD.NumberTextureShootUp >= 2) CUPHEAD.SwitchTextureShootUp = -1;
		if(CUPHEAD.NumberTextureShootUp <= 0) CUPHEAD.SwitchTextureShootUp =  1;
		CUPHEAD.NumberTextureShootUp += CUPHEAD.SwitchTextureShootUp;
		CUPHEAD.Counter = 0;
}
}
	
if(CUPHEAD.PositionX == Ground)CUPHEAD.SwitchPose = 0;
if(CUPHEAD.PositionY < 0)CUPHEAD.PositionY = 0;
if(CUPHEAD.PositionY > 840)CUPHEAD.PositionY = 840;
}

DrawMatrix( &CUPHEAD.Cuphead_offset, &CUPHEAD.Cuphead, 100, 100, CUPHEAD.PositionY, CUPHEAD.PositionX, 0, 0, CUPHEAD.Turn, CUPHEAD.Color);
}
